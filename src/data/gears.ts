import type { TierId } from "./tiers";

export type GearCategory =
  | "sprinkler"
  | "watering"
  | "tool"
  | "gadget"
  | "mushroom"
  | "misc";

export interface Gear {
  slug: string;
  name: string;
  emoji: string;
  category: GearCategory;
  /** 1x Robux price. null when the item is not sold for Robux. */
  price1x: number | null;
  /** 3x bundle price. null when not offered. */
  price3x: number | null;
  /** 10x bundle price. null when not offered. */
  price10x: number | null;
  /** Optional Sheckle price. */
  shecklesPrice: number | null;
  /** Tier when the gear has a clear one. */
  tier?: TierId;
  /** Short effect description. null when wiki marks it as TBD. */
  effect: string | null;
  /** One-liner blurb (own wording, sourced from wiki). */
  blurb: string;
}

